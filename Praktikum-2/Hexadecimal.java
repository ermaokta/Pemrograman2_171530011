public class Hexadecimal {
    public static void main(String args[]) {
        int x = 0x0001;
        int y = 0xfffffff;
        int z = 0xDeadCafe;

        System.out.println("x = " + x);
        System.out.println("y = " + y);
        System.out.println("z = " + z);
    }
}
//hexadesimal ditandai dengan 0x